//En este ejemplo veremos como incrementar o decrementar un valor a. 

let a = 1; 

document.write('El valor es: ', a);
document.write('<br>');
//incrementamos el valor en 1.
a++;
document.write('El nuevo valor de a es: ', a);
document.write('<br>');
//incrementar en el valor que estimemos conveniente. 
a += 5;
document.write('El nuevo valor de a es: ', a);
document.write('<br>');
//restamos 1 el valor de a.
a--;
document.write('El valor de a es: ',a);
document.write('<br>');
//restamos la cantidad deseada.
a -= 4;
document.write('El valor de a es:', a);
document.write('<br>');
//dividimos.
a /= 2;
document.write('El valor de a es:',a);
document.write('<br>');
//multiplicamos.
a *= 3;
document.write('El valor de a es: ',a);
document.write('<br>');
a **= 3;
document.write('El valor de a es: ',a);
document.write('<br>');
